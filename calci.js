function add(x, y) {
    return x + y;
}

function sub(x, y) {
    return x - y;
}

function mult(x, y) {
    return x * y;
}

function div(x, y) {
    return x / y;
}

console.log("Addition:", add(10, 5));
console.log("Subtraction:", sub(10, 5));
console.log("Multiplication:", mult(10, 5));
console.log("Division:", div(10, 5));
